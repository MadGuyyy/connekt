package com.flipkart.connekt.receptors.routes.ws

import akka.actor.ActorRef
import com.flipkart.connekt.commons.iomodels.ConnektRequest

object PullNotificationBus {

  private val eventBus = new PullNotificationEventBusImpl

  def subscribe(destination:String, actorRef:ActorRef): Boolean = eventBus.subscribe(actorRef, destination)

  def unsubscribe(destination:String, actorRef:ActorRef): Boolean = eventBus.unsubscribe(actorRef, destination)

  def publish(message:ConnektRequest): Unit = eventBus.publish(message)

}
